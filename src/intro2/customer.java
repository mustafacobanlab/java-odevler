package intro2;

public class customer {
	private int id;
	private String costomerNumberString;
	private String phone;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getCostomerNumberString() {
		return costomerNumberString;
	}

	public void setCostomerNumberString(String costomerNumberString) {
		this.costomerNumberString = costomerNumberString;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

}
